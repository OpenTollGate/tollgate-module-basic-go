func generateCGI(schema *UISchema, structs map[string]*StructDef) string {
	var b strings.Builder

	b.WriteString("#!/bin/sh\n")
	b.WriteString("# /www/cgi-bin/tollgate-api - TollGate config API for LuCI\n")
	b.WriteString("# Auto-generated by luci-gen. Do not edit manually.\n\n")

	b.WriteString("CONFIG=\"/etc/tollgate/config.json\"\n")
	b.WriteString("IDENTITIES=\"/etc/tollgate/identities.json\"\n\n")

	writeCGIHelpers(&b)
	writeCGIParseQuery(&b)
	writeCGIDispatch(&b, schema, structs)

	return b.String()
}

func writeCGIHelpers(b *strings.Builder) {
	b.WriteString(cgiHelpersBlock)
}

const cgiHelpersBlock = `json_header() {
    printf "Content-Type: application/json\r\n\r\n"
}

json_esc() {
    tr -d '\000-\010\013\014\016-\037' | awk '
    BEGIN { ORS=""; printf "\"" }
    {
        gsub(/\\/, "\\\\")
        gsub(/"/, "\\\"")
        gsub(/\t/, "\\t")
        gsub(/\r/, "\\r")
    }
    NR > 1 { printf "\\n" }
    { printf "%s", $0 }
    END { printf "\"" }
    '
}

run_cmd() {
    "$@" 2>&1 || true
}

get_logs() {
    logread 2>/dev/null | grep 'daemon.err tollgate-wrt' | tail -n 300 | sed -E 's/^.*tollgate-wrt\[[0-9]+\]: //' || true
}

`

func writeCGIParseQuery(b *strings.Builder) {
	b.WriteString(cgiParseBlock)
}

const cgiParseBlock = `action=""
qs="${QUERY_STRING:-}"
old_ifs="$IFS"
IFS="&"
for p in $qs; do
    k="${p%%=*}"
    v="${p#*=}"
    case "$k" in
        action) action="$v" ;;
    esac
done
IFS="$old_ifs"

body=""
if [ "${CONTENT_LENGTH:-0}" -gt 0 ] 2>/dev/null; then
    body=$(dd bs=1 count="$CONTENT_LENGTH" 2>/dev/null)
fi

`

func writeCGIDispatch(b *strings.Builder, schema *UISchema, structs map[string]*StructDef) {
	b.WriteString("case \"$action\" in\n\n")

	writeCGIReadAction(b)
	writeCGILogsAction(b)
	writeCGIValidateAction(b, schema, structs)
	writeCGISaveAction(b, schema)
	writeCGIWalletFundAction(b)
	writeCGINetworkActions(b)
	writeCGIReadIdentitiesAction(b)
	writeCGISaveIdentitiesAction(b)
	writeCGIDefaultAction(b)

	b.WriteString("esac\n")
}

func writeCGIReadAction(b *strings.Builder) {
	b.WriteString(`read)
    cf=$(cat "$CONFIG" 2>/dev/null || echo "{}")
    idf=$(cat "$IDENTITIES" 2>/dev/null || echo "{}")
    wl=$(run_cmd /usr/bin/tollgate wallet balance)
    wi=$(run_cmd /usr/bin/tollgate wallet info)
    vr=$(run_cmd /usr/bin/tollgate version)
    st=$(run_cmd /usr/bin/tollgate status)
    lg=$(get_logs)

    json_header
    printf '{"ok":true,"config":%s,"identities":%s,"wallet":' "$cf" "$idf"
    printf '%s\n' "$wl" | json_esc
    printf ',"wallet_info":'
    printf '%s\n' "$wi" | json_esc
    printf ',"version":'
    printf '%s\n' "$vr" | json_esc
    printf ',"status":'
    printf '%s\n' "$st" | json_esc
    printf ',"logs":'
    printf '%s\n' "$lg" | json_esc
    printf '}'
    ;;

`)
}

